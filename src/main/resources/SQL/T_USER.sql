DROP TABLE IF EXISTS T_USER;
CREATE TABLE T_USER(
	USER_ID	VARCHAR(20) PRIMARY KEY,
	ACCOUNT_NAME VARCHAR(200) NOT NULL COMMENT 'account_name',
	ACCOUNT_ID VARCHAR(20) NOT NULL COMMENT 'account_id',
	PSD VARCHAR(20) NOT NULL COMMENT 'password',
	BIRTHDAY DATETIME NOT NULL COMMENT 'birthday',
	USER_ROLE CHAR(2) COMMENT 'User Role',
	USER_AUTHORITY CHAR(2) COMMENT 'User Authority',
	MENU_GROUP_AUTHORITY CHAR(2) COMMENT 'Menu Group Authority',
	MENU_SUB_MAX_AUTHORITY CHAR(2) COMMENT 'Menu Sub Max Authority',
	INS_DATE DATETIME COMMENT 'Insert Time',
	INS_USER_ID VARCHAR(20) COMMENT 'Insert User',
	INS_PROGRAM VARCHAR(20) COMMENT 'Insert Program',
	UPD_DATE DATETIME COMMENT 'Update Time',
	UPD_USER_ID VARCHAR(20) COMMENT 'Update User',
	UPD_PROGRAM VARCHAR(20) COMMENT 'Update Program',
	LOCK_VERSION BIGINT COMMENT 'Update Version'
) CHARACTER SET UTF8 COMMENT = 'User';

